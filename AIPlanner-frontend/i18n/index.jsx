const TranslationContext = createContext(null);

function normalizeLanguage(value) {
  return supportedLanguages.includes(value) ? value : "en";
}

function getStoredLanguage() {
  if (typeof window === "undefined") return "en";
  return normalizeLanguage(window.localStorage.getItem(LANGUAGE_STORAGE_KEY));
}

function translate(language, key, params = {}) {
  const template = translations[language]?.[key] ?? translations.en[key] ?? key;
  return Object.entries(params).reduce(
    (text, [name, value]) => text.replaceAll(`{{${name}}}`, String(value)),
    template
  );
}

function enumLabel(t, value) {
  const normalized = String(value ?? "").toUpperCase();
  const map = {
    LOW: "low",
    MEDIUM: "medium",
    HIGH: "high",
    TODO: "todo",
    IN_PROGRESS: "inProgress",
    DONE: "done",
    COMPLETED: "completed",
    ACTIVE: "active"
  };
  return map[normalized] ? t(map[normalized]) : value;
}

function dateLocale(language) {
  if (language === "az") return "az-Latn-AZ";
  if (language === "ru") return "ru-RU";
  return "en-US";
}

function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(getStoredLanguage);

  useEffect(() => {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo(() => ({
    language,
    setLanguage: (nextLanguage) => setLanguageState(normalizeLanguage(nextLanguage)),
    t: (key, params) => translate(language, key, params),
    enumLabel: (value) => enumLabel((key, params) => translate(language, key, params), value),
    dateLocale: dateLocale(language)
  }), [language]);

  return <TranslationContext.Provider value={value}>{children}</TranslationContext.Provider>;
}

function useTranslation() {
  const context = useContext(TranslationContext);
  if (!context) {
    throw new Error("useTranslation must be used within LanguageProvider");
  }
  return context;
}

export {
  LANGUAGE_STORAGE_KEY,
  LanguageProvider,
  languageOptions,
  supportedLanguages,
  useTranslation
};
